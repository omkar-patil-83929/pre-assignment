#include<stdio.h>
int main()
{
	char c;
	int ascii;
	printf("\n enter a character");
	scanf("%c",&c);
	printf("\n  decimal value is %d",c);
	printf("\n octal value is %o",c);
	printf("\n hexadecimal value is %x",c);
	printf("\n Enter ascii value");
	scanf("%d",&ascii);
	printf("\n character for ascii value is %c",ascii);
	return 0;
}
	
